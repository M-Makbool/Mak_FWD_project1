int delayms(int t);

int delayms(int t)
{
	
	volatile uint64_t x, y,f;
	for (x = 0; x < t; x++)
		for (y = 0; y < 790; y++)
			f=y;
	
	return f;
}

int main()
{
	SYSCTL->RCC |= 0x02400540;
	SYSCTL->RCGCGPIO |= 0x20;
	GPIOF->DIR |= 0xFF;
	GPIOF->DEN |= 0xFF;
	while (1)
	{
		GPIO_Mak_WritePin(PORTF,PIN2,1U);
		delayms(1000);
		GPIO_Mak_WritePin(PORTF,PIN3,1U);
		delayms(1000);
		GPIO_Mak_WritePin(PORTF,PIN2,0U);
		delayms(1000);
		GPIO_Mak_WritePin(PORTF,PIN3,0U);
		delayms(1000);
	}
}

